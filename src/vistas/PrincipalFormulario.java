    public void centrarVentana(JInternalFrame frame) {
        desktopPane.add(frame);
        Dimension dimPrincipalF = desktopPane.getSize();
        Dimension dimVentaF = frame.getSize();
        frame.setLocation((dimPrincipalF.width - dimVentaF.width) / 2, (dimPrincipalF.height - dimVentaF.height) / 2);
        frame.show();
    }

